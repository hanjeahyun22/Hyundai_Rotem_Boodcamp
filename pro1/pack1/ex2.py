# 산술 연산
print(4 + 5)

# 문자열 더하기 연산
print('4' + "5")
print("한국"*5)         # 한국한국한국한국한국

# ++, -- 와 같은 증감 연산자 X
print('누적')
a = 10
a = a + 1
a += 1          # -=, *=, /= 
print(f"a는 {a}")

print(--a)      # -1 * -1 = +1
print(-a)       # 부호 변경
print(a * -1)   # 부호 변경

print('1' + '1')
# print(('1' + '1') + 1)                   # '문자 11' + 숫자 1 : type error
print((int('1') + int('1')) + 1)             # int('1') -> 문자 '1'을 정수형 숫자 1 로 바꿔줌
print((float('1') + int('1')) + 1)            # float('1') -> 문자 '1'을 실수형 숫자 1.0 으로 바꿔줌
print(str(1 + 1) + '1')                     # 1+1 숫자를 str함수로 '2'로 문자열로 변환
print('boolean 처리 : ', bool(True), bool(False))   # bool(True) = True, bool(False) = False

# bool : python에서 데이터가 있으면 True, 없으면 False
print(bool(1), bool(12.3), bool('ok'), bool([12]))
print(bool(0), bool(0.0), bool(''), bool([]), bool(None))

