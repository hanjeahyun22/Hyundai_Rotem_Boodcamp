# 연산자 계산 순서
# () -> ** -> 단항 -> 산술(*, /, //, % -> +, -) -> 관계 -> 논리(not -> and -> or) -> =
reslut = 3 + 4 * 5 + (2 + 3) / 2
print(reslut)

print(5 > 3, 5 == 3, 5 !=3)

print(5 > 3 and 4 < 3, 5 > 3 or 4 < 3, not(5 >= 3))

# and 연산 순위가 or 보다 높음 -> and 먼저 계산
print(True or False and False)
print(True and False or False)
print('&&&&&&'*10)
