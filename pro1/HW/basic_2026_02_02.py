# 문8) 1 ~ 1000 사이의 소수(1보다 크며 1과 자신의 수 이외에는 나눌 수 없는 수)와 그 갯수를 출력
# 힌트: 이 문제는 반복이 두 단계다. 2부터 1000까지 하나씩 검사한다. 각 숫자마다 소수인지 확인한다.
# 그래서 while 안에 while 구조가 필요하다.
# --------------------------------------------------------------------------------------------------------------------------------------
print('\nQ8 ============================================================================')

num = 2
count = 0
while num <= 1000:
    i = 2
    is_prime = True             # 소수 여부 판별 변수
    while i < num:              # 소수는 자기 자신만으로 나누어 떨어져야 하므로, 조건식에 등호는 들어가면 안됨
        if num % i == 0:
            is_prime = False
            break
        i += 1
    if is_prime:
        print(num, end = ' ')
        count += 1
    num += 1
print('\ncount : ', count)


