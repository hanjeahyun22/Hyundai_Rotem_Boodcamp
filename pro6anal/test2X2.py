# ######################################################################################################################
#                                         교차분석 (Chi-Square Test) 가설 검정
# ######################################################################################################################

# 1. 정의
#   - 범주형 자료(Nominal/Ordinal Data)를 대상으로 두 변수 간의 연관성을 분석하는 기법
#   - 관찰된 빈도(Observed Frequency)가 기대되는 빈도(Expected Frequency)와 의미있게 다른지 검증

# 2. 카이제곱 분포 (Chi-Square Distribution)
#   - 분산이 퍼져있는 모습을 분포로 만든 것
#   - 항상 양수 값만 가지며, 오른쪽으로 꼬리가 긴 형태를 띰
#   - 공식: X² = ∑ [ (관측값 - 기대값)² / 기대값 ]
#     * 관측값과 기대값의 차이가 클수록 X² 값이 커지며, 이는 두 변수 간의 상관관계가 높음을 의미

# 3. 주요 분석 종류
#   1) 적합도 검정 (Goodness of Fit Test) - 일원 카이제곱
#      - 표본 집단의 분포가 특정 이론적 분포를 따르는지 검정
#      - 예: 주사위를 60번 던졌을 때 각 눈이 10번씩(기대값) 골고루 나왔는가?
#
#   2) 독립성 검정 (Test of Independence) - 이원 카이제곱
#      - 두 범주형 변수 사이에 관계가 있는지(독립적인지) 검정
#      - 예: 성별(남/여)에 따라 선호하는 정당(A/B/C)이 다른가?
#
#   3) 동질성 검정 (Test of Homogeneity) - 이원 카이제곱
#      - 서로 다른 모집단에서 추출한 표본들의 분포가 서로 동일한지 검정
#      - 예: 지역별(서울/부산)로 특정 정책에 대한 찬반 비율이 같은가?

# 4. 가설 설정 예시 (독립성 검정 기준)
#   - 귀무가설(H0): 두 변수는 서로 독립적이다. (연관성이 없다)
#   - 대립가설(H1): 두 변수는 서로 독립적이지 않다. (연관성이 있다)

# 5. 분석 시 주의사항
#   - 데이터는 빈도수(Count) 형태여야 함
#   - 기대 빈도가 5 미만인 셀이 전체의 20%를 넘지 않아야 신뢰도가 높음
