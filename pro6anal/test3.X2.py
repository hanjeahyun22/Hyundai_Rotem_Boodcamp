# 귀무가설 : 기대치와 관찰치는 차이가 없다.(= 이 주사위는 게임에 적합하다.)
# 대립가설 : 기대치와 관찰치는 차이가 있다.(= 이 주사위는 게임에 적절하지 않다.)

import pandas as pd
import scipy.stats as stats

data = [4, 6, 17, 16, 8, 9]
print(stats.chisquare(data))        # statistic=np.float64(14.200000000000001), pvalue=np.float64(0.014387678176921308)

# 판정 : 유의수준(alpha = 0.05) < p_value(= 0.014387678176921308) 이므로, 귀무가설(H0)을 기각하고, 대립가설(H1) 채택
# -->> 검정에 사용된 데이터는 우연히 발생한 것이 아니라, 필연적인 원인에 의해 발생함.
# -->> "이 주사위는 게임에 적합하지 않다" 라는 의견을 받아들임.

