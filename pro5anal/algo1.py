# 예) 4, 6 : 4와 6은 2로 나누어 떨어지므로, 2(GCD)
def gcd(a, b):
    i = min(a, b)
    while True:                 # False로 바뀌기 전까지 계속 반복
        if a % i == 0 and b % i == 0:
            return i
        i = i - 1
# print(gcd(4, 6))
# print(gcd(24, 16))
print(gcd(81, 27))


print("\n\n---------------------- 유클리드 방식 ----------------------")
def gcdFunc2(a, b):
    if b == 0:
        return a
    return gcdFunc2(b, a % b)       # 좀 더 작은값으로 재귀 호출

print(gcdFunc2(4, 6))
print(gcdFunc2(24, 16))