print("\n\n---------------------- 최대값 구하는 알고리즘 ----------------------") 
def find_max(a):
    n = len(a)
    max_v = a[0]
    for i in range(1, n):                   # 이미 max_v에 0번째 index값은 들어가 있으므로, 1번째~n-1번째 index까지 for 반복
        if a[i] > max_v:
            max_v = a[i]
    return max_v

print(find_max(d))


