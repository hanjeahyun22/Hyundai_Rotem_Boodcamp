print("\n\n---------------------- 일반적인 for 반복문 ----------------------") 
def sum_n(n):
    s = 0
    for i in range(1, n+1):
        s = s + i
    return s

print(sum_n(10))

print("\n\n---------------------- 가우스 덧셈 공식 ----------------------")
def sum_n2(n):
    return n * (n+1) // 2

print(sum_n2(10))

